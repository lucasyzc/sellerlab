/**
 * Prebuild script: queries git for the latest commit date of each route group's
 * source files, then writes lib/generated/sitemap-dates.ts so that sitemap.ts
 * can import accurate lastModified values instead of hardcoded strings.
 *
 * Usage:  node scripts/generate-sitemap-dates.mjs
 * Hooks:  prebuild / predev (see package.json)
 */

import { execSync } from "child_process";
import { writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

// ---------------------------------------------------------------------------
// Route-group → source paths mapping
// Directories (trailing /) track all files underneath.
// Shared deps (like lib/fee-seo.ts) are listed so that a change to shared
// fee logic also bumps the sitemap date for every calculator page.
// ---------------------------------------------------------------------------

const SHARED_FEE_DEPS = ["lib/fee-seo.ts"];

const ROUTE_GROUPS = {
  home: ["app/page.tsx", "app/layout.tsx"],
  about: ["app/about/"],
  contact: ["app/contact/"],
  glossary: ["app/glossary/"],

  "ebay-fee-calculator": ["app/ebay-fee-calculator/", ...SHARED_FEE_DEPS],
  "ebay-pricing-calculator": [
    "app/ebay-pricing-calculator/",
    ...SHARED_FEE_DEPS,
  ],
  "amazon-fee-calculator": ["app/amazon-fee-calculator/", ...SHARED_FEE_DEPS],
  "amazon-pricing-calculator": [
    "app/amazon-pricing-calculator/",
    ...SHARED_FEE_DEPS,
  ],
  "tiktok-shop-fee-calculator": [
    "app/tiktok-shop-fee-calculator/",
    ...SHARED_FEE_DEPS,
  ],
  "shopify-fee-calculator": [
    "app/shopify-fee-calculator/",
    ...SHARED_FEE_DEPS,
  ],
  "walmart-fee-calculator": [
    "app/walmart-fee-calculator/",
    ...SHARED_FEE_DEPS,
  ],

  "ebay-title-optimizer": ["app/ebay-title-optimizer/"],

  compare: ["app/compare/"],
  updates: ["app/updates/"],

  "privacy-policy": ["app/privacy-policy/"],
  "terms-of-service": ["app/terms-of-service/"],
};

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function gitLastModified(paths) {
  try {
    const out = execSync(`git log -1 --format=%cI -- ${paths.join(" ")}`, {
      cwd: ROOT,
      encoding: "utf-8",
      stdio: ["pipe", "pipe", "pipe"],
    }).trim();
    return out || null;
  } catch {
    return null;
  }
}

function toDateString(isoOrNull) {
  if (!isoOrNull) return new Date().toISOString().slice(0, 10);
  return isoOrNull.slice(0, 10);
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

const dates = {};

for (const [group, paths] of Object.entries(ROUTE_GROUPS)) {
  dates[group] = toDateString(gitLastModified(paths));
}

// Build the output TS source
const OUTPUT_DIR = join(ROOT, "lib", "generated");
const OUTPUT_FILE = join(OUTPUT_DIR, "sitemap-dates.ts");

mkdirSync(OUTPUT_DIR, { recursive: true });

const lines = [
  "// Auto-generated — do not edit manually.",
  "// Regenerated by: node scripts/generate-sitemap-dates.mjs",
  "",
  "export const SITEMAP_DATES: Record<string, string> = {",
  ...Object.entries(dates).map(([k, v]) => `  "${k}": "${v}",`),
  "} as const;",
  "",
  "export function sitemapDate(group: string): Date {",
  '  const d = SITEMAP_DATES[group] ?? new Date().toISOString().slice(0, 10);',
  "  return new Date(d);",
  "}",
  "",
];

writeFileSync(OUTPUT_FILE, lines.join("\n"), "utf-8");

console.log("✔ Generated sitemap dates →", OUTPUT_FILE);
for (const [k, v] of Object.entries(dates)) {
  console.log(`  ${k.padEnd(30)} ${v}`);
}
